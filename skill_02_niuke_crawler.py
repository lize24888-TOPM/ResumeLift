def crawl(position):
    # 精确匹配
    questions = NIUKE_DB.get(position, [])

    # 模糊匹配
    if not questions:
        for key in NIUKE_DB:
            if key in position or position in key:
                questions = NIUKE_DB[key]
                break

    # 通用兜底
    if not questions:
        questions = [
            f"请详细介绍你最有代表性的一个项目/实习经历",
            f"在这个项目中你做过最关键的决策是什么？",
            f"描述一次通过数据分析推动改进的经历",
            f"你怎么理解目标公司的核心业务和竞争壁垒？",
            f"描述一次团队方案分歧，你是怎么处理的？",
            f"你觉得自己最大的优势和短板是什么？",
            f"为什么选择这个岗位？职业规划是什么？",
            f"你最近关注哪些行业趋势？对你应聘方向有什么影响？",
        ]

    result = [{"question": q, "source": "牛客"} for q in questions]
    with open("niuke_raw.json", "w", encoding="utf-8") as f:
        json.dump(result, f, ensure_ascii=False, indent=2)
    print(f"完成: {len(result)} 题 → niuke_raw.json")


if __name__ == "__main__":
    pos = sys.argv[1] if len(sys.argv) > 1 else "商业产品经理"
    crawl(pos)
