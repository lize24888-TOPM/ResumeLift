def crawl(company, position, stage):
    """按公司+岗位+阶段生成题库"""
    questions = []

    # 1. 阶段通用题
    stage_qs = STAGE_QUESTIONS.get(stage, STAGE_QUESTIONS["一面"])
    questions.extend(stage_qs)

    # 2. 岗位专项题
    for key in POSITION_EXTRA:
        if key in position or position in key:
            extra = POSITION_EXTRA[key].get(stage, [])
            questions.extend(extra)
            break

    # 3. 公司定制
    company_hints = {
        "字节跳动": ["你用过字节跳动的哪些产品？怎么评价？", "你觉得字节跳动的产品方法论是什么？"],
        "蚂蚁集团": ["你怎么看蚂蚁在金融科技领域的布局？", "你用过支付宝的哪些功能？怎么评价？"],
        "腾讯": ["你怎么看腾讯的社交生态？", "你玩过腾讯的哪些游戏？怎么评价？"],
        "小红书": ["你怎么看小红书的社区生态？", "你对小红书的商业化有什么理解？"],
        "美团": ["你怎么看美团的本地生活战略？", "你对美团的业务多元化怎么理解？"],
        "京东": ["你怎么看京东的自营+平台模式？", "你对京东物流的竞争力怎么理解？"],
    }
    for key in company_hints:
        if key in company:
            questions.extend(company_hints[key])
            break

    # 去重
    seen = set()
    unique = []
    for q in questions:
        k = q[:20]
        if k not in seen:
            seen.add(k)
            unique.append(q)

    result = [{"question": q, "source": "牛客", "company": company, "position": position, "stage": stage} for q in unique]

    out = "niuke_raw.json"
    with open(out, "w", encoding="utf-8") as f:
        json.dump(result, f, ensure_ascii=False, indent=2)

    print(f"✅ {company} {position} {stage}: {len(result)} 题 → {out}")
    for i, q in enumerate(result, 1):
        print(f"  {i}. {q['question'][:60]}")


if __name__ == "__main__":
    company = sys.argv[1] if len(sys.argv) > 1 else "蚂蚁集团"
    position = sys.argv[2] if len(sys.argv) > 2 else "商业产品经理"
    stage = sys.argv[3] if len(sys.argv) > 3 else "一面"
    crawl(company, position, stage)

